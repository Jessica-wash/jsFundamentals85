// Comparison Operators

// equal to
'3' == 3;
console.log('3' == 3); //true

// Strict eqiual to
// compares the datetype and value of the two values to check if they are equal

console.log('3' === 3); //false

// Not equal to
console.log('3' != 3); //false

// Strict not equal
console.log('3' !== 3); //true

//Greater than
console.log(3 > 2); //true
console.log(3 > 23); //false

// Less Than
console.log(2 < 3 ); //true

//Greater than or equal to
console.log(3 >= 2 ); //true

// Less than or equal to
console.log(2 <= 3); //true

//'And' Operator
2 && 3;

//'Or' Operator
2 || 3;

